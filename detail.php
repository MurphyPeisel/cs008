        <section class="habitats">
            <h3>Natural Habitats</h3>
            <p class="indent">
                When you recycle, you're putting something that you
                have used back into the system, which means that
                less of the materials that make up that product need 
                to be harvested (which is very costly on natural 
                habitats of animals). Additionally, other resources 
                are required to produce completely new items that
                could have been recycled. The fossil fuel use
                of tools used to harvest contribute towards the 
                changing of the climate, which would cause a large
                scale displacement of many, many animals on Earth.
                On an even simpler level, when waste doesn't get
                properly disposed of, it can end up in open landfills
                and even be dumped into the ocean. Animals, not knowing
                any better, will often eat and/or get stuck in 
                trash, which can lead to serious damage, even death.
            </p>
