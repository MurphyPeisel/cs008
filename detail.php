                to landfills is the production of leachate. Leachate is
                concentrated wastewater that consists of the various
                chemicals that can be found in a landfill. Leachate can
                travel and contaminate the local bodies of water,
                rendering it non-potable. Recycling as much as possible
                minimizes the amount of trash that finds its way into
                landfills, helping to prevent the creation of leachate
                and the negative effects that are associated with it.
                The prevention of leachate production is more cost
                effective than cleaning up leachate as it's produced,
                so there's economical reason as well.
